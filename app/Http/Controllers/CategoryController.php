     * Store a newly created resource in storage.
     */
    public function store(StoreCategoryRequest $request)
    {
        if (request()->input("title")) {
            if (request()->input("parent") == 0) {
                Category::create(["name" => $request->title]);
            } else {
                Category::create(["name" => $request->title,
                    "parent_id" => $request->parent]);
            }
        }
        return back();
    }

